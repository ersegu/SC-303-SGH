    public Reportes(Citas citasFrame, ArrayList<CitaMOD> listaCitas) {
        initComponents();
        this.citasFrame = citasFrame;

        if (listaCitas == null || listaCitas.isEmpty()) {
            txtReportes.setText("No hay citas disponibles.");
        } else {
            StringBuilder sb = new StringBuilder();
            for (CitaMOD cita : listaCitas) {
                sb.append("CÓDIGO: ").append(cita.getCodigoCita()).append("\n");
                sb.append("MÉDICO: ").append(cita.getMedico().nombre_completo()).append("\n");
                sb.append("PACIENTE: ").append(cita.getPaciente().nombre_completo()).append("\n");
                sb.append("FECHA: ").append(cita.getFecha()).append("\n");
                sb.append("HORA: ").append(cita.getHora()).append("\n");
                sb.append("=======================================\n");
            }
            txtReportes.setText(sb.toString());
        }

        addWindowListener(new WindowAdapter(){
            @Override
            public void windowClosing(WindowEvent e) {
                volverCitas();
            }
        });
    }

    public void volverCitas() {
        this.citasFrame.setVisible(true);
        dispose();
    }

